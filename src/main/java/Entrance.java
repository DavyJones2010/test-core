public class Entrance {
	String name;
	int age;
	Address addr;

	public Entrance(String name, int age, Address addr) {
		super();
		this.name = name;
		this.age = age;
		this.addr = addr;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public Address getAddr() {
		return addr;
	}

	public void setAddr(Address addr) {
		this.addr = addr;
	}

	@Override
	public String toString() {
		return "Entrance [name=" + name + ", age=" + age + ", addr=" + addr + "]";
	}

}
